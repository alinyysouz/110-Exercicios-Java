package exerciciosjava;

public class Ex77 {

	public static void main(String[] args) {
		// 77) Crie um programa que exiba os números ímpares de 50 a 1.
	
		 System.out.println("Números ímpares de 50 a 1:");

	        
	        for (int i = 50; i >= 1; i--) {
	           
	            if (i % 2 != 0) {
	                System.out.print(i + " ");
	            }
	        }

	        System.out.println();
	
	
	
	}

}
