package exerciciosjava;

public class Ex66 {

	public static void main(String[] args) {
		// 66) Faça um programa que exiba os números de 1 a 10.
		
		System.out.println("Números de 1 a 10 usando loop 'for':");
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
	}

}
